<?php

/*
## switch conditional statement
// replacement of elseif statement



*/ 

$grade = "D";

switch ($grade) {
    case 'A':
        echo "Ans is A";
        break;

    case 'B':
        echo "Ans is B";
         break;
            
    case 'C':
        echo "Ans is C";
          break;

    case 'D':
        echo "Ans is D";
            break;
    
    default:
        echo "Invalid case";
        break;
}


?>